t=-4:0.001:4;
f1=@(t) (-t-4).*(t>=-4 & t<=-3);
f2=@(t) (t+2).*(t>=-3 & t<=-1);
f3=@(t) (3*t+4).*(t>=-1 & t<=0);
f4=@(t) (-3*t+4).*(t>=0 & t<=1);
f5=@(t) (2-t).*(t>=1 & t<=3);
f6=@(t) (-4+t).*(t>=3 & t<=4);
plot(t,f1(t),t,f2(t),t,f3(t),t,f4(t),t,f5(t),t,f6(t));
legend('-t - 4','t + 2','3t + 4','-3t + 4','2 - t','-4 + t');
grid on;